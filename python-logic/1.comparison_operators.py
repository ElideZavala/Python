# -----------**## Comparison Operators ##**------------------------------------------------

print(14 > 4)  # True
print(14 >= 4)  # True
print(14 < 4)  # False
print(14 <= 4)  # False
print(14 == 4)  # False
print(14 == "14")  # False
print(14 != "14")  # True
print("cat" > "dog")  # False
print("cat" == "CAT")  # False

# Numeric Representation
print(ord("c"))  # 99
print(ord("C"))  # 67
print(ord("a"))  # 97
print(ord("A"))  # 65
print(ord("t"))  # 116
print(ord("T"))  # 84
