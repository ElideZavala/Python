# ********___Example 13___********
temperature = 40

while temperature >= 20:
    print(
        f"It es a good day to go out when the temperature is {temperature}")
    temperature -= 5

# aqui se esta decrementando la variable temperature en 5, por lo que el while loop se ejecutara 5 veces, ya que la condicion es que la temperatura sea mayor o igual a 20
